def assign_grade(score):
    if score > 90: return "A"
    elif score >= 80: return "B"
    elif score >= 70: return "C"
    elif score >= 60: return "D"
    return "F"

def is_leap(year):
    # Bug: only checks divisible by 4
    if year % 4 == 0:
        return True
    return False

